#include "../include/libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*lst_copy;

	if (!lst || !*lst || !del)
		return ;
	while (lst && *lst)
	{
		lst_copy = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = lst_copy;
	}
	*lst = NULL;
}
